    private void bGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGravarActionPerformed
         
        String modelo = tfModelo.getText();
        String placa = tfPlaca.getText();
        String anoString = tfAno.getText();
        String cidade = tfCidade.getText();
        TipoVeiculo tipo = (TipoVeiculo) cTipo.getSelectedItem();
        String mensagem = null;
        int ano = 0;
        Calendar calendario = Calendar.getInstance();
        int anoAtual = calendario.get(Calendar.YEAR);
        if (modelo.length() > 0 && placa.length() > 0 && anoString.length() > 0 && cidade.length() > 0) {
            try {
                ano = Integer.parseInt(anoString);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Informe somente numero para o ano", "Aten√ß√£o", JOptionPane.WARNING_MESSAGE);
            }
            if (placa.length() > 9) {
                JOptionPane.showMessageDialog(null, "Informe corretamente a placa", "Aten√ß√£o", JOptionPane.WARNING_MESSAGE);
            } else {
                if (ano >= 1900 && ano <= anoAtual) {
                    if (veiculo == null) {
                        veiculo = new Veiculo(placa, cidade, modelo, ano, tipo);
                    } else {
                        veiculo.setAno(ano);
                        veiculo.setCidade(cidade);
                        veiculo.setModelo(modelo);
                        veiculo.setPlaca(placa);
                        veiculo.setTipo(tipo);
                        mensagem = "veiculo alterado";
                    }

                    try {
                        veiculoDAO.alterar(veiculo);
                        JOptionPane.showMessageDialog(null, mensagem);

                        tfAno.setText("");
                        tfCidade.setText("");
                        tfModelo.setText("");
                        tfPlaca.setText("");
                        cTipo.setSelectedIndex(0);

                        bPesquisarActionPerformed(evt);

                        veiculo = null;

                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Informe uma data valida", "Aten√ß√£o", JOptionPane.WARNING_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Informe todos os campos.", "Aten√ß√£o", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_bGravarActionPerformed

